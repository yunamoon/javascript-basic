/**
 * variable 선언하기
 * 
 * 1) var _ 더 이상 사용하지 않는다.
 * 2) let _ 
 * 3) const 
 */


// let과 var은 추후 값을 변경 할 수 있다.
var name = 'yuna moon';
console.log(name);

var age = 29;
console.log(age);

let sister = 'sena moon';
console.log(sister);

sister = 'hanna moon';
console.log(sister);

